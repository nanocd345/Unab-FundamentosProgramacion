def mostrar_menu():
    print("Calculadora Aritmética")
    print("1. Sumar")
    print("2. Restar")
    print("3. Multiplicar")
    print("4. Dividir")
    print("5. Salir")


while True:
    mostrar_menu()

    opcion = int(input("Selecciona una opción (1-5): "))

    a=int(input("digame el numero 1: "))
    b=int(input("ahora el numero 2: "))

    if opcion == 5:
        print("chao :)")
        break
    elif opcion == 1:
        sumar(a, b)
    elif opcion == 2:
        restar(a, b)
    elif opcion == 3:
        multiplicar(a, b)
    elif opcion == 4:
        dividir(a, b)
    else:
        print("pero ponga un numero entre el 1 y el 5 ._.")
